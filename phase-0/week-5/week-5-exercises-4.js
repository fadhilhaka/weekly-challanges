//Logic Challenge: Graduates

function graduates (students) {
  // Code disini
  var grad = {}
  for (var a = 0; a < students.length; a++) {
    grad[students[a].class] = [];
  }
  for (var b = 0; b < students.length; b++) { 
    if (students[b].score >= 75) {
      var gradStudents = {
      name: students[b].name,
      score: students[b].score,
    }
     //console.log(b, gradStudents)
    grad[students[b].class].push(gradStudents)
    }
  }
  return grad
}

